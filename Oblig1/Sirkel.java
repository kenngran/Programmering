public class Sirkel {
	public static double diameter(double r) {
		return r*2;
	}
	
	public static double omkrets(double r) {
		return Math.PI * 2 * r;
	}
	
	public static double areal(double r) {
		return Math.PI * r * r;
	}
	public static void main(String[] args) {
		System.out.println();
	}
}
